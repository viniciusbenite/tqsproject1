public class Indexes {

    private Baqi baqi;

    public Indexes() {
    }

    public Baqi getBaqi() {
        return baqi;
    }

    public void setBaqi(Baqi baqi) {
        this.baqi = baqi;
    }

    @Override
    public String toString() {
        return "Indexes{" +
                "baqi=" + baqi +
                '}';
    }
}
